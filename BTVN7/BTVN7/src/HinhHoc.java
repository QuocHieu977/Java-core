public class HinhHoc {
    public int dienTichHCN(int length, int width) {
        return length*width;
    }

    public int dienTichHV(int a) {
        return a*a;
    }

    public double chuViHCN(int length, int width) {
        return (double) (length+width)/2;
    }

    public int chuViHV(int a) {
        return a*4;
    }
}
